package com.digoshop.app.module.looksales.tool;

public class SaleShopBean {
	// 图片地址
	private String imgurl;
	// 商品名字
	private String shopname;
	// 剩余时间
	private String remaining_time;
	// 所需人民币
	private String shop_rmb;
	// 所需积分
	private String shop_score;

	public String getImgurl() {
		return imgurl;
	}

	public void setImgurl(String imgurl) {
		this.imgurl = imgurl;
	}

	public String getShopname() {
		return shopname;
	}

	public void setShopname(String shopname) {
		this.shopname = shopname;
	}

	public String getRemaining_time() {
		return remaining_time;
	}

	public void setRemaining_time(String remaining_time) {
		this.remaining_time = remaining_time;
	}

	public String getShop_rmb() {
		return shop_rmb;
	}

	public void setShop_rmb(String shop_rmb) {
		this.shop_rmb = shop_rmb;
	}

	public String getShop_score() {
		return shop_score;
	}

	public void setShop_score(String shop_score) {
		this.shop_score = shop_score;
	}

}
